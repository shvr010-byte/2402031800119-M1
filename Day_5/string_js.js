let s="Hello, World!";
document.write(s.length); // Output: 13
document.write("<br>" + s.toUpperCase()); // Output: "HELLO, WORLD!"
document.write("<br>" + s.toLowerCase());          
document.write("<br>" + s.substring(0, 5)); // Output: "Hello"
document.write("<br>" + s.indexOf("World")); // Output: 7
document.write("<br>" + s.replace("World", "JavaScript")); // Output: "Hello, JavaScript!"
document.write("<br>" + s.split(", ")); // Output: ["Hello", "World!"]  
document.write(s.slice(0,5)); // Output: "Hello"
